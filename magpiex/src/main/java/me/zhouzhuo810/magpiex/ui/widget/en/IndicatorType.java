/**
 * Indicator的类型枚举
 *
 * @author zz
 * @date 2016/8/22
 */
public enum IndicatorType {
    /**
     * 圆点类型，适用于APP引导页
     */
    RoundPoint,
    /**
     * 纯文字类型
     */
    TabWithText,
    /**
     * 纯图标类型
     */
    TabWithIcon,
    /**
     * 图片+文字类型
     */
    TabWithIconAndText
}
